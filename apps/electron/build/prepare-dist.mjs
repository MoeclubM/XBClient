import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { resolveAppVersion } from './resolve-version.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const electronDir = path.resolve(__dirname, '..')
const repoRoot = path.resolve(electronDir, '../..')
const configDir = path.join(electronDir, 'resources', 'config')
const dstProps = path.join(configDir, 'local.properties')
const srcProps = path.join(repoRoot, 'local.properties')

const DESKTOP_KEYS = [
  ['xbclient.defaultApiUrl', 'XBCLIENT_DEFAULT_API_URL'],
  ['xbclient.appName', 'XBCLIENT_APP_NAME'],
  ['xbclient.userAgent', 'XBCLIENT_USER_AGENT'],
  ['xbclient.oauthCallbackScheme', 'XBCLIENT_OAUTH_CALLBACK_SCHEME'],
  ['xbclient.websiteUrl', 'XBCLIENT_WEBSITE_URL'],
  ['xbclient.privacyPolicyUrl', 'XBCLIENT_PRIVACY_POLICY_URL'],
  ['xbclient.userAgreementUrl', 'XBCLIENT_USER_AGREEMENT_URL'],
]

function readProps(file) {
  if (!fs.existsSync(file)) return {}
  const entries = {}
  for (const line of fs.readFileSync(file, 'utf8').split(/\r?\n/)) {
    const text = line.trim()
    if (!text || text.startsWith('#') || text.startsWith('!')) continue
    const idx = text.search(/[:=]/)
    if (idx <= 0) continue
    entries[text.slice(0, idx).trim()] = text.slice(idx + 1).trim()
  }
  return entries
}

function pickValue(propKey, envKey, fileProps) {
  const fromEnv = process.env[envKey]?.trim()
  if (fromEnv) return fromEnv
  if (fileProps[propKey]?.trim()) return fileProps[propKey].trim()
  if (fileProps[envKey]?.trim()) return fileProps[envKey].trim()
  return ''
}

function requireDesktopConfig(fileProps) {
  const missing = []
  const values = {}
  for (const [propKey, envKey] of DESKTOP_KEYS) {
    const value = pickValue(propKey, envKey, fileProps)
    values[propKey] = value
    if (
      ['XBCLIENT_DEFAULT_API_URL', 'XBCLIENT_APP_NAME', 'XBCLIENT_USER_AGENT', 'XBCLIENT_OAUTH_CALLBACK_SCHEME'].includes(
        envKey,
      ) &&
      !value
    ) {
      missing.push(envKey)
    }
  }
  if (missing.length > 0) {
    throw new Error(
      `缺少桌面端构建配置：${missing.join(', ')}。请在 CI secrets 或 local.properties 中设置（无需 AdMob 相关字段）。`,
    )
  }
  return values
}

function writeProps(file, values) {
  const lines = [
    '# generated by apps/electron/build/prepare-dist.mjs',
    ...DESKTOP_KEYS.map(([key]) => `${key}=${values[key]}`),
  ]
  fs.writeFileSync(file, `${lines.join('\n')}\n`)
}

const fileProps = readProps(srcProps)
const config = requireDesktopConfig(fileProps)

fs.mkdirSync(configDir, { recursive: true })
writeProps(dstProps, config)
console.log('wrote resources/config/local.properties from env/local.properties')

const { electronVersion } = resolveAppVersion()
const pkgPath = path.join(electronDir, 'package.json')
const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'))
pkg.version = electronVersion
fs.writeFileSync(pkgPath, `${JSON.stringify(pkg, null, 2)}\n`)
console.log(`electron package version -> ${electronVersion}`)

const scheme = config['xbclient.oauthCallbackScheme']
const productName = config['xbclient.appName']
const executableName = productName.replace(/[^\w.-]+/g, '') || 'XBClient'
let yml = fs.readFileSync(path.join(__dirname, 'electron-builder.yml'), 'utf8')
yml = yml.replace(/^productName:.*$/m, `productName: ${productName}`)
yml = yml.replace(/^  executableName:.*$/gm, `  executableName: ${executableName}`)
if (scheme && !yml.includes('protocols:')) {
  yml += `\nprotocols:\n  - name: XBClient OAuth\n    schemes:\n      - ${scheme}\n`
}
const ghRepo = process.env.GITHUB_REPOSITORY?.trim()
if (ghRepo && !yml.includes('\npublish:')) {
  const [owner, repo] = ghRepo.split('/')
  if (owner && repo) {
    yml += `\npublish:\n  provider: github\n  owner: ${owner}\n  repo: ${repo}\n  vPrefixedTagName: true\n`
  }
}
fs.writeFileSync(path.join(__dirname, 'electron-builder.generated.yml'), yml)
